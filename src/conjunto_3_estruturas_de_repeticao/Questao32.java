public class Questao32 {

    /*
    32. Exiba todos os números de 1 a 500, um ao lado do outro com um espaço em branco de
    separação.
     */

    public static void main(String[] args) {
        System.out.println("Exercício 32 - Exiba todos os números de 1 a 500, um ao lado do outro com um espaço em branco de separação.");
        for (int i = 1; i <= 500; i++) {
            System.out.print(i + " ");
        }
    }
}
